/** lấy danh sách các quy tắc GET /api/rule */
export async function rule(
  params: {
    // query
    /** số trang hiện tại */
    current?: number;
    /** Dung lượng trang */
    pageSize?: number;
  },
  options?: { [key: string]: any },
) {
  return request<API.RuleList>('/api/rule', {
    method: 'GET',
    params: {
      ...params,
    },
    ...(options || {}),
  });
}

